package main;

public class calcu {

    public static int sumar(int num, int num1){
        return num + num1;
    }
    public static int resstar(int num, int num2){
        return num - num2;
    }
    public static int div(int num, int num2){
        return num / num2;
    }
    public static int por(int num, int num2){
        return  num * num2;
    }

}
